    /**
     * @param glyphName
     * @return
     */
    static int getUV(String glyphName) {
        Integer UV = (Integer) h.get(glyphName);
        if (UV != null)
            return UV.intValue();
        if (glyphName.startsWith("G")) {
            try {
                String s = glyphName.substring(1);
                int i = Integer.parseInt(s);
                return i;
            } catch (Exception ex) {
            }
        }
        return -1;
    }

